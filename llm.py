import os
from azure.ai.inference import ChatCompletionsClient
from azure.ai.inference.models import SystemMessage, UserMessage
from azure.core.credentials import AzureKeyCredential

# Токен GitHub (должен быть в переменной окружения или задан явно)
GITHUB_TOKEN = ""
if not GITHUB_TOKEN:
    raise ValueError("Установите переменную окружения GITHUB_TOKEN")

client = ChatCompletionsClient(
    endpoint="https://models.github.ai/inference",
    credential=AzureKeyCredential(GITHUB_TOKEN),
)

def paraphrase_single_article(article_text: str) -> str:
    """
    Перефразирует одну новостную статью на русском языке.
    """
    system_prompt = (
        '''Ты — опытный редактор, который пересказывает тексты по-человечески, без ощущения, что их писал робот. Перепиши приведённый ниже текст статьи своими словами. Сохрани все факты, цифры, логику, термины и структуру абзацев, но сделай так, чтобы текст звучал живо, естественно и без шаблонов.

Правила:

Не заменяй слово словом по словарю — перестраивай фразы, меняй порядок слов, объединяй или разбивай предложения там, где это уместно.

Избегай штампов вроде «всё чаще поднимается вопрос», «следует отметить», «вышеупомянутый», «таким образом» (кроме финального вывода, если он был в оригинале).

Добавляй лёгкие разговорные интонации, где это не вредит смыслу: частицы «ведь», «вот», «даже», вводные слова «кстати», «например», «в общем», короткие связки «и», «а», «но».

Не бойся иногда оставить фразу почти как в оригинале, если она уже звучит по-человечески. Главное — не делать текст стерильно-переиначенным.

Не пиши слишком гладко и правильно — можно чуть смягчить или усилить эмоциональную окраску, добавить живую пунктуацию (тире, восклицательный знак только если он был в исходнике или очень к месту).

Никаких комментариев про то, что ты делаешь, никаких «вот перефразированный текст» — только сам результат.

Следи за грамматикой, но если где-то выйдет чуть более короткая или рубленая фраза — это даже плюс.

Объём пусть примерно совпадает с оригиналом (плюс-минус 20%).

Текст не должен быть такой же или похожий на оригинал.

Каждый абзац начинай с <p> и заканчивай </p>.

Текст для перефразирования:'''
    )

    response = client.complete(
        messages=[
            SystemMessage(content=system_prompt),
            UserMessage(content=article_text),
        ],
        model="gpt-4o-mini",   # 150 запросов/день на бесплатном тарифе
        max_tokens=4000,        # можно регулировать
    )
    return response.choices[0].message.content


def paraphrase_single_title(article_text: str) -> str:
    """
    Перефразирует один заголовок статьи на русском языке.
    """
    system_prompt = (
        "Ты — профессиональный редактор новостей. Перефразируй заголовок статьи так, "
        "чтобы сохранить все ключевые факты, но изменить форму изложения. "
        "Ответ должен быть на русском языке, грамотным и нейтральным."
    )

    response = client.complete(
        messages=[
            SystemMessage(content=system_prompt),
            UserMessage(content=article_text),
        ],
        model="gpt-4o-mini",   # 150 запросов/день на бесплатном тарифе
        max_tokens=400,        # можно регулировать
    )
    return response.choices[0].message.content
